function show(action, bg_color, header_text, count) {
    var which_table = "";
    localStorage.header_text = header_text;
    localStorage.api = action;
    localStorage.bg_color = bg_color;
    // localStorage.api = action;
    if ($("#" + count).text() == "0") {
        swal.fire({
            title: "No data",
            text: "no data available",
            type: "error",
            showCloseButton: true,
            confirmButtonColor: "#C12E2A",
            allowOutsideClick: true,

        })
    }
    else {
        if(action == "getActive_dash" || action == "getInactive_dash" || action == "getUnassigned_dash" || action == "getComm" || action == "getDecomm"){
            $("#table_columns_container").html(all_vw_columns)
            $("#overlay-assets-added").show();
        }
        else if( action == "getUsers_dash" ){
            $("#table_columns_container").html(users_columns)
            $("#overlay-assets-added").show();
        }
        else if( action == "getPending_dash" ){
            $("#table_columns_container").html(log_pending_columns);
            $("#overlay-assets-added").show();
        }
        else if(action == "getMoved_dash"){
            document.getElementById('modal-header').innerHTML = localStorage.header_text;
            $("#exportBtn").addClass("btn-lg");
            $("#modal-header,#exportBtn").addClass(localStorage.bg_color);
            $("#loader-overlay").css("display", "block");
            getData(localStorage.api, "");
        }

    }
}

function hide(element) {
    $(element).fadeOut();
}




